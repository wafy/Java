public class Operators {
    public static void main(String[] args) {
//        String mystring = "Hello" + "Codeit!!";
        int a = 9;
        int b = 5;

        System.out.println("숫자 연산자");
        System.out.println(a + " + " + b + " = " + (a + b));
        System.out.println(a + " - " + b + " = " + (a - b));
        System.out.println(a + " * " + b + " = " + (a * b));
        System.out.println(a + " / " + b + " = " + ((double)a / b));
        System.out.println(a + " % " + b + " = " + (a % b));

    }
}
